    private void cargarActividadesEnTabla() {
    DefaultTableModel modelo = (DefaultTableModel) tbActividades.getModel();
    modelo.setRowCount(0); // Limpiar tabla

    List<Actividad> lista = actividadDAO.listarActividades();
    for (Actividad act : lista) {
        modelo.addRow(new Object[]{
            act.getIdActividad(),
            act.getNombre(),
            act.getDescripcion(),
            act.getDuracion(),
            act.getIdLugarTuristico(),
            act.getIdProveedor(),
            act.getEstado()
        });
    }
}
    
    public void refrescarTabla() {
        cargarActividadesEnTabla();
    }



