    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    int filaSeleccionada = tbActividades.getSelectedRow();

    if (filaSeleccionada != -1) {
        int idActividad = (int) tbActividades.getValueAt(filaSeleccionada, 0);
        Actividad actividad = actividadDAO.obtenerActividadPorId(idActividad);

        if (actividad != null) {
            FormularioActividad formulario = new FormularioActividad(this, actividad);
            formulario.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró la actividad.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecciona una actividad para editar.");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    int filaSeleccionada = tbActividades.getSelectedRow();

    if (filaSeleccionada != -1) {
        int id = (int) tbActividades.getValueAt(filaSeleccionada, 0); // Columna 0 = ID

        int confirmacion = JOptionPane.showConfirmDialog(this, 
            "¿Estás seguro de eliminar esta actividad?", 
            "Confirmar eliminación", 
            JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {
            if (actividadDAO.eliminarActividad(id)) {
                JOptionPane.showMessageDialog(this, "✅ Actividad eliminada correctamente.");
                refrescarTabla(); // Recarga la tabla
            } else {
                JOptionPane.showMessageDialog(this, "❌ Error al eliminar la actividad.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

