private void cargarLugaresEnTabla() {
    List<LugarTuristico> lista = lugarDAO.listarLugaresTuristicos();
    DefaultTableModel modelo = (DefaultTableModel) tbLugares.getModel();
    modelo.setRowCount(0);
    for (LugarTuristico lt : lista) {
        modelo.addRow(new Object[]{
            lt.getIdLugarTuristico(),
            lt.getNombre(),
            lt.getTipo(),
            lt.getDescripcion(),
            lt.getIdDestino(),     // asegúrate de usar el getter correcto
            lt.getIdProveedor(),   // asegúrate de usar el getter correcto
            lt.getEstado()
        });
    }
}

