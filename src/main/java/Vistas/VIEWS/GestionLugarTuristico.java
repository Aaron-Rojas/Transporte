    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    int fila = tbLugares.getSelectedRow();
    if (fila >= 0) {
        int id = (int) tbLugares.getValueAt(fila, 0);
        LugarTuristico lugar = lugarDAO.obtenerLugarPorId(id);
        FormularioLugarTuristico form = new FormularioLugarTuristico(this, true, lugar);
        form.setVisible(true);
        cargarLugaresEnTabla();
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione un lugar para editar.");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    int fila = tbLugares.getSelectedRow();
    if (fila >= 0) {
        int id = (int) tbLugares.getValueAt(fila, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "¿Desea eliminar este lugar turístico?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            if (lugarDAO.eliminarLugarTuristico(id)) {
                JOptionPane.showMessageDialog(this, "Lugar eliminado correctamente.");
                cargarLugaresEnTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar lugar.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione un lugar para eliminar.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

